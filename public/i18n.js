let LANG = 'en';

function detectLang() {
  // ?lang=en ile dogrudan secilebilir: baglanti paylasirken ve belge/ekran
  // goruntusu hazirlarken islevsel.
  try {
    const q = new URLSearchParams(location.search).get('lang');
    if (q && I18N[q]) return q;
  } catch { /* eski tarayici */ }

  const saved = localStorage.getItem('sd_lang');
  if (saved && I18N[saved]) return saved;
  for (const l of (navigator.languages || [navigator.language || 'en'])) {
    const base = String(l).toLowerCase().split('-')[0];
    if (I18N[base]) return base;
  }
  return 'en';
}

function setLang(lang) {
  LANG = I18N[lang] ? lang : 'en';
  localStorage.setItem('sd_lang', LANG);
  document.documentElement.lang = LANG;
  document.documentElement.dir = RTL_LANGS.includes(LANG) ? 'rtl' : 'ltr';
  applyI18n();
}

// Ceviri eksikse Ingilizceye, o da yoksa anahtarin kendisine duser.
function t(key, vars) {
  const dict = I18N[LANG] || I18N.en;
  let s = dict[key];
  if (s === undefined) s = I18N.en[key];
  if (s === undefined) return key;
  if (vars) for (const k of Object.keys(vars)) s = s.split('{' + k + '}').join(vars[k]);
  return s;
}

// data-i18n / data-i18n-title / data-i18n-ph iceren her ogeyi gunceller.
function applyI18n(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  scope.querySelectorAll('[data-i18n-title]').forEach((el) => { el.title = t(el.dataset.i18nTitle); });
  scope.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.placeholder = t(el.dataset.i18nPh); });
  if (typeof onLangChanged === 'function') onLangChanged();
}
